package LoopsDemo;

public class Forloop {

	public static void main(String[] args) {

		System.out.println("Loop started");
		
		for(int i=0;i<=10;i++)
		{
			System.out.println("The value of I is " +i);
		}
		
		System.out.println("Loop end");
		
		
		System.out.println("******");
	}
	
	
	
	
	
	

}
