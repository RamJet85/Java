package Conditionalstatements;

public class operators {

	public static void main(String[] args) {
		
		// Example for == and != operators relational operators are used to  perform operations
		
		// Operators are used to perform operations on variables and values.
		
		int a = 10;
		int b = 20;
		
		if(a == b)
		{
			System.out.println("The values of a & b are same");
		}
		else
		{
			System.out.println("The values of a & b are not same");
		}
		
		
		
		if(a != b)
		{
			System.out.println("The values of a & b are equal");
		}
		else
		{
			System.out.println("The values of a & b are not same");
		}
		

	}

}
