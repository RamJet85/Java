package ConditionalStatemen;

public class Switch {

	public static void main(String[] args) {
		
		
		int year = 1993;
	
		switch(year) {
		case 1:
			System.out.println("1993");
			break;
		case 2:
			System.out.println("1988");
			break;
		case 3:
			System.out.println("9876");
			break;
		case 4:
		    System.out.println("0988");
			break;
				
			default: 
			System.out.println("None of the above ");
				
		}

	}
}
